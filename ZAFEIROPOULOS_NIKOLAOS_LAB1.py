#create dict
a_dict = {"h":30,"g":28,"f":28,"e":16,"d":14,"c":14,"b":12,"a":10}
print("Main dict before edit: " +str(a_dict))


def removeDictDuplicates():
  result = {}
  for key,value in a_dict.items():
    if value not in result.values():
        result[key] = value
  print("The dict with remove duplicate : "+str(result))


print()
#calling func for remove duplicates in dict
removeDictDuplicates()


def sortDictValue():
  a_new_dict = {'h': 30, 'g': 28, 'e': 16, 'd': 14, 'b': 12, 'a': 10}
  print("Sorted dict by value")
  for key in sorted(a_new_dict.keys()):
    print("%s: %s" % (key, a_new_dict[key]))


print()
#print sorting for dict
sortDictValue()


def sortDictKey():
  a_new_dict = {'h': 30, 'g': 28, 'e': 16, 'd': 14, 'b': 12, 'a': 10}
  print("Sorted dict by key")
  for key, value in sorted(a_new_dict.items(), key=lambda item: item[1]):
    print("%s: %s" % (key, value))


