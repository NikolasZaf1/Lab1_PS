a_list = [30,28,28,16,14,14,12,10]

#printing my list
print("Main list before remove duplicates: " +str(a_list))


def removeDuplicates():
  res = [] 
  for i in a_list: 
    if i not in res: 
        res.append(i) 
  # printing list after removal  
  print ("The list after removing duplicates : " + str(res)) 

print()
#calling func removeDuplicates
removeDuplicates()


